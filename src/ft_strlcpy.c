#include "libft.h"
#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	if (ft_strlen(dst) >= size -1)
	{
		while (size - 1 > 0)
		{
			*(dst + size) = *(src + size);
			size--;
		}
		*(dst + size) = '\0';
	}
	return (ft_strlen(src));
}
