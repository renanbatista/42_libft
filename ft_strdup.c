#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*str;
	int		index;

	index = 0;
	str = (char *) malloc(ft_strlen(s) * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (*(s + index))
	{
		*(str + index) = *(s + index);
		index++;
	}
	return (str);
}
