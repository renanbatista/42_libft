#include "libft.h"
#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*str;

	str = (char *) s;
	while (*(str) && n > 1)
	{
		str++;
		n--;
		if (*(str) == c)
			return (str);
	}
	return (NULL);
}
